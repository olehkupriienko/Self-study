s = input().lower() #додаткова функція переведення символів в нижній регістр
counte1r = 0
counte2r = 0

for i in range(len(s)):
    if s[i] in 'ауоыиэяюёе':    # АУОЫИЭЯЮЁЕ
        counte1r += 1
    if s[i] in 'бвгджзйклмнпрстфхцчшщ': #БВГДЖЗЙКЛМНПРСТФХЦЧШЩ
        counte2r += 1
print('Количество гласных букв равно', counte1r)
print('Количество согласных букв равно', counte2r)
